export const sum = (a:number, b:number) => {
    return(a+b)
}

export const difference = (a:number, b:number) => {
    return(a-b)
}

export const multiplication = (a:number, b:number) => {
    return(a*b)
}

export const splitIntroWord = (sentence:string) => {
    return(
        sentence.split(' ')
    )

}